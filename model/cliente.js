class Cliente {
    nombre;
    nacimiento;
    correo;
    telefono;
    descripcion;
    
    constructor(nombre,nacimiento,correo,telefono,descripcion) {
        this.nombre = nombre;
        this.nacimiento = nacimiento;
        this.correo = correo;
        this.telefono = telefono;
        this.descripcion = descripcion;
    }
}